package ru.epam.javacore.lesson_12_io_nio.lesson.a_3_xml.model;

public class Child {
    private String name;
    private String hobby;
    private boolean schoolBoy;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHobby() {
        return hobby;
    }

    public void setHobby(String hobby) {
        this.hobby = hobby;
    }

    public boolean isSchoolBoy() {
        return schoolBoy;
    }

    public void setSchoolBoy(boolean schoolBoy) {
        this.schoolBoy = schoolBoy;
    }
}
